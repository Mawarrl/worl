#include <stdlib.h>
#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int				len;
	unsigned int	i;
	char			*tab;

	i = 0;
	if (!((*f) && s))
		return (NULL);
	len = ft_strlen(s);
	if (!(tab = (char*)malloc(sizeof(char) * (len + 1))))
		return (0);
	while (s[i])
	{
		tab[i] = (*f)(i, s[i]);
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
